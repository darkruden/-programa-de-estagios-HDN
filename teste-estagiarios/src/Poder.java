
public class Poder {
	private int id;
	private String poder;

	public Poder(int id, String poder) {
		super();
		this.id = id;
		this.poder = poder;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPoder() {
		return poder;
	}
	public void setPoder(String poder) {
		this.poder = poder;
	}
}
